<template>
  <div class="carousel">
    <div class="carousel-inner">
      <Carousel-indicators
        v-if="indicators"
        :total="slides.length"
        :current-index="currentSlide"
        @switch="switchSlide($event)"
      >
      </Carousel-indicators>
      <Carousel-item
        v-for="(slide, index) in slides"
        :slide="slide"
        :key="`item-${index}`"
        :current-slide="currentSlide"
        :index="index"
        :direction="direction"
        @mouseenter="stopSlideTimer"
        @mouseout="startSlideTimer"
      >
      </Carousel-item>
      <Carousel-controls
        v-if="controls"
        @pre="pre"
        @next="next"
      ></Carousel-controls>
    </div>
  </div>
</template>

<script>
import CarouselItem from './CarouselItem.vue'
import CarouselControls from './CarouselControls.vue'
import CarouselIndicators from './CarouselIndicators.vue'
export default {
  props: {
    slides: {
      type: Array,
      required: true,
    },
    controls: {
      type: Boolean,
      default: true,
    },
    indicators: {
      type: Boolean,
      default: true,
    },
    interval: {
      type: Number,
      default: 5000,
    },
  },
  components: { CarouselItem, CarouselControls, CarouselIndicators },
  data: () => ({
    currentSlide: 0,
    slideInterval: null,
    direction: 'right',
  }),
  methods: {
    setCurrentSlide(index) {
      this.currentSlide = index
    },
    pre(step = -1) {
      const index =
        this.currentSlide > 0
          ? this.currentSlide + step
          : this.slides.length - 1
      this.setCurrentSlide(index)
      this.direction = 'left'
      this.startSlideTimer()
    },
    _next(step = 1) {
      const index =
        this.currentSlide < this.slides.length - 1
          ? this.currentSlide + step
          : 0
      this.setCurrentSlide(index)
      this.direction = 'right'
    },
    next() {
      this._next()
      this.startSlideTimer()
    },
    startSlideTimer() {
      this.stopSlideTimer()
      this.slideInterval = setInterval(() => {
        this._next()
      }, this.interval)
    },
    stopSlideTimer() {
      clearInterval(this.slideInterval)
    },
    switchSlide(index) {
      const step = index - this.currentSlide
      if (step > 0) {
        this.next(step)
      } else {
        this.pre(step)
      }
    },
  },
  mounted() {
    // this._next()
    this.startSlideTimer()
  },
  beforeDestroy() {
    this.stopSlideTimer()
  },
}
</script>

<style scoped>
.carousel {
  @apply flex justify-center;
}
.carousel-inner {
  @apply relative w-[900px] h-[400px] overflow-hidden;
}
</style>